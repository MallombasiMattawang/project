<?php

namespace frontend\controllers;

use Yii;
use frontend\models\Religion;
use frontend\models\access\ReligionAccess;
use frontend\models\form\ReligionForm;
use frontend\models\search\ReligionSearch;
use common\base\Controller;
use yii\web\HttpException;
use yii\helpers\Url;
use dmstr\bootstrap\Tabs;
use cornernote\returnurl\ReturnUrl;

/**
 * ReligionController implements the CRUD actions for Religion model.
 */
class ReligionController extends Controller
{

	/**
	 * @var boolean whether to enable CSRF validation for the actions in this controller.
	 * CSRF validation is enabled only when both this property and [[Request::enableCsrfValidation]] are true.
	 */
	public $enableCsrfValidation = false;

	/**
	 * Lists all active Religion models.
	 * @return mixed
	 */

	public function actionIndex()
	{
		if(Yii::$app->user->can('/religion/index')) {
			if (ReligionAccess::allowIndex() == FALSE)
			{
				throw ReligionAccess::exception('index');
			}

			$searchModel = new ReligionSearch;
			$dataProvider = $searchModel->searchIndex($_GET);

			Tabs::clearLocalStorage();

			Url::remember();
			\Yii::$app->session['__crudReturnUrl'] = null;

			return $this->render('index', [
					'dataProvider'	 => $dataProvider,
					'searchModel'	 => $searchModel,
			]);
		}else {
			throw ReligionAccess::exception('You dont have permission to access');
		}	

	}

	/**
	 * Lists deleted Religion models.
	 * @return mixed
	 */
	public function actionDeleted()
	{
		if(Yii::$app->user->can('/religion/delete')) {
			if (ReligionAccess::allowDeleted() == FALSE)
			{
				throw ReligionAccess::exception('deleted');
			}

			$searchModel = new ReligionSearch;
			$dataProvider = $searchModel->searchDeleted($_GET);

			Tabs::clearLocalStorage();

			Url::remember();
			\Yii::$app->session['__crudReturnUrl'] = null;

			return $this->render('deleted', [
					'dataProvider'	 => $dataProvider,
					'searchModel'	 => $searchModel,
			]);
		}else {
			throw ReligionAccess::exception('index');
		}	

	}

	/**
	 * Displays a single Religion model.
	 * @param integer $id
	 *
	 * @return mixed
	 */
	public function actionView($id)
	{
		if(Yii::$app->user->can('/religion/view')) {
			$model = $this->findModel($id);

			if ($model->operation->allowView == FALSE)
			{
				throw $model->operation->exception('view');
			}

			\Yii::$app->session['__crudReturnUrl'] = ReturnUrl::getUrl(Url::previous());
			Url::remember();
			Tabs::rememberActiveState();

			return $this->render('view', [
					'model' => $model,
			]);
		}else {
			throw ReligionAccess::exception('index');
		}			

	}

	/**
	 * Creates a new Religion model.
	 * If creation is successful, the browser will be redirected to the 'view' page.
	 * @return mixed
	 */
	public function actionCreate()
	{
		if (ReligionAccess::allowCreate() == FALSE)
		{
			throw ReligionAccess::exception('create');
		}

		$model = new ReligionForm();

		try
		{
			if ($model->load($_POST) && $model->save())
			{
				return $this->redirect(ReturnUrl::getUrl(Url::previous()));
			}
			else if (!\Yii::$app->request->isPost)
			{
				$model->load($_GET);
			}
		}
		catch (\Exception $e)
		{
			$msg = (isset($e->errorInfo[2])) ? $e->errorInfo[2] : $e->getMessage();
			$model->addError('_exception', $msg);
		}

		return $this->render('create', ['model' => $model]);

	}

	/**
	 * Updates an existing Religion model.
	 * If update is successful, the browser will be redirected to the 'view' page.
	 * @param integer $id
	 * @return mixed
	 */
	public function actionUpdate($id)
	{
		$model = $this->findForm($id);

		if ($model->operation->allowUpdate == FALSE)
		{
			throw $model->operation->exception('update');
		}

		if ($model->load($_POST) && $model->save())
		{
			return $this->redirect(ReturnUrl::getUrl(Url::previous()));
		}
		else
		{
			return $this->render('update', [
					'model' => $model,
			]);
		}

	}

	/**
	 * Deletes an existing Religion model.
	 * If deletion is successful, the browser will be redirected to the 'index' page.
	 * @param integer $id
	 * @return mixed
	 */
	public function actionDelete($id)
	{
		try
		{
			$model = $this->findModel($id);

			if ($model->operation->allowDelete == FALSE)
			{
				throw $model->operation->exception('delete');
			}

			$model->delete();
		}
		catch (\Exception $e)
		{
			$msg = (isset($e->errorInfo[2])) ? $e->errorInfo[2] : $e->getMessage();

			\Yii::$app->getSession()->setFlash('error', $msg);

			return $this->redirect(ReturnUrl::getUrl(Url::previous()));
		}

		// TODO: improve detection
		$isPivot = strstr('$id', ',');

		if ($isPivot == true)
		{
			return $this->redirect(ReturnUrl::getUrl(Url::previous()));
		}
		else if (isset(\Yii::$app->session['__crudReturnUrl']) && \Yii::$app->session['__crudReturnUrl'] != '/')
		{
			Url::remember(null);

			$url = \Yii::$app->session['__crudReturnUrl'];
			$url = ReturnUrl::getUrl($url);

			\Yii::$app->session['__crudReturnUrl'] = null;

			return $this->redirect($url);
		}
		else
		{
			return $this->redirect(['index']);
		}

	}

	/**
	 * Finds the Religion model based on its primary key value.
	 * If the model is not found, a 404 HTTP exception will be thrown.
	 * @param integer $id
	 * @return Religion the loaded model
	 * @throws HttpException if the model cannot be found
	 */
	protected function findModel($id)
	{
		if (($model = Religion::findOne($id)) !== null)
		{
			return $model;
		}
		else
		{
			throw new HttpException(404, 'The requested page does not exist.');
		}

	}

	/**
	 * Finds the Religion model based on its primary key value for form purpose.
	 * If the model is not found, a 404 HTTP exception will be thrown.
	 * @param integer $id
	 * @return Religion the loaded model
	 * @throws HttpException if the model cannot be found
	 */
	protected function findForm($id)
	{
		if (($model = ReligionForm::findOne($id)) !== null)
		{
			return $model;
		}
		else
		{
			throw new HttpException(404, 'The requested page does not exist.');
		}

	}

}
